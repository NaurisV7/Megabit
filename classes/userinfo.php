<?php

class UserInfo extends Db {

    protected function getAllEmails () {
        $sql = "SELECT * FROM emails";
        $result = $this->connect()->query($sql);
        $numRows = $result->num_rows;
        if ($numRows > 0) {
            while ($row = $result->fetch_assoc()) {
                $data[] = $row;
            }
            return $data;
        }
    }
}